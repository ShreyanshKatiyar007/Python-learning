list = [10,25,17]

list.append(20)     # adds an element at the end
print(list)

list.sort()         # sorts in ascending order
print(list)

list.sort(reverse = True)   # sorts in descending order
print(list)

list.reverse()      # reverse list
print(list)

list.insert(1,30)   # insert element at given index
print(list)

list.remove(25)     # remove first occurence of element
print(list)

list.pop(2)         # remove element at given index
print(list)
