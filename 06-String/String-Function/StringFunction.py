# String Functions
str = "i am studying python"

print(str.endswith("on"))  # returns true if string ends with substring.

print(str.capitalize())  # capitalizes first character.

print(str.replace("python","javascript"))  # replaces all occurrences of old with new.

print(str.find("s"))  # returns first index of first occurrence.

print(str.count("i"))  # count the occurrence of substring.
